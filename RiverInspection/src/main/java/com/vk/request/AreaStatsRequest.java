public class AreaStatsRequest {
    private String areaName;
    private String total;
    private String al;
    private String alp;
    private String fal;
    private String falp;
    private String score;

    public String getAreaName() {
        return areaName;
    }

    public void setAreaName(String areaName) {
        this.areaName = areaName;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getAl() {
        return al;
    }

    public void setAl(String al) {
        this.al = al;
    }

    public String getAlp() {
        return alp;
    }

    public void setAlp(String alp) {
        this.alp = alp;
    }

    public String getFal() {
        return fal;
    }

    public void setFal(String fal) {
        this.fal = fal;
    }

    public String getFalp() {
        return falp;
    }

    public void setFalp(String falp) {
        this.falp = falp;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
